<?php

// 冒泡排序
$arr = [3, 1, 4, 2, 5, 9, 8, 6, 7, 0];
$n = count($arr);
for ($j = 0; $j < $n - 1; $j++) {
    // 每一趟最大的不一定在最上面，但最小得一定在最下面所以后面可以省略
    /******
     *****
     ****
     ***
     **
     */
    for ($i = 0; $i < $n - $j - 1; $i++) {
        if ($arr[$i + 1] < $arr[$i]) {
            $temp = $arr[$i];
            $arr[$i] = $arr[$i + 1];
            $arr[$i + 1] = $temp;
        }
    }
}
print_r($arr);